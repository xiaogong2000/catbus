const dictionaries: Record<string, Dict> = { en, "zh-CN": zhCN, "zh-TW": zhTW, ja };

export function getDictionary(locale: string): Dict {
  return dictionaries[locale] || en;
}

/** Get a translated string. Falls back to English, then to the key itself. */
export function t(dict: Dict, key: string): string {
  return dict[key] || en[key] || key;
}
